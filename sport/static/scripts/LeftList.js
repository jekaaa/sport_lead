var sport = document.getElementsByName("sport")[0];

function Click() {
    var l = document.getElementsByName("leftList")[0];


    if(l.style.display == "none") {
        l.style.display = "block";
        sport.style.background = "#29980e";
    }
    else {
        l.style.display = "none";
        sport.style.background = "black";
    }
}
